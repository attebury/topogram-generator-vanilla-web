#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const packageRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

function assertMarker(root, marker) {
  if (!fs.existsSync(path.join(root, marker))) {
    throw new Error(`Expected ${marker} under package root ${root}`);
  }
}

function resolveContainedPath(root, relativePath, label) {
  if (!relativePath || path.isAbsolute(relativePath) || /^[a-z][a-z0-9+.-]*:/i.test(relativePath)) {
    throw new Error(`${label} must be a relative path inside package root: ${relativePath}`);
  }
  const normalized = path.posix.normalize(relativePath.replace(/\\/g, "/"));
  if (normalized === "." || normalized === ".." || normalized.startsWith("../")) {
    throw new Error(`${label} escapes package root: ${relativePath}`);
  }
  const absolute = path.resolve(root, normalized);
  const back = path.relative(root, absolute);
  if (back.startsWith("..") || path.isAbsolute(back)) {
    throw new Error(`${label} escapes package root: ${relativePath}`);
  }
  return { normalized, absolute };
}

assertMarker(packageRoot, "package.json");
assertMarker(packageRoot, "topogram-generator.json");
assertMarker(packageRoot, "llms.txt");

const llmsPath = resolveContainedPath(packageRoot, "llms.txt", "llms input").absolute;
const outputPath = resolveContainedPath(packageRoot, "llms-full.txt", "llms-full output").absolute;

export function localMarkdownLinksFromLlms(text, options = {}) {
  const requiredText = options.includeOptional ? text : text.split(/^## Optional$/m)[0] || text;
  const links = [];
  const seen = new Set();
  const pattern = /\[[^\]]+\]\(([^)]+)\)/g;
  let match;
  while ((match = pattern.exec(requiredText))) {
    const href = match[1].split("#")[0];
    if (!href || /^https?:/i.test(href) || href.startsWith("mailto:")) continue;
    if (!href.endsWith(".md")) continue;
    const { normalized } = resolveContainedPath(packageRoot, href, "llms.txt link");
    if (seen.has(normalized)) continue;
    seen.add(normalized);
    links.push(normalized);
  }
  return links;
}

export function buildLlmsFullText() {
  const llmsText = fs.readFileSync(llmsPath, "utf8").trimEnd();
  const links = localMarkdownLinksFromLlms(llmsText);
  const sections = [
    "<!-- Generated by scripts/build-llms-full.mjs; do not edit by hand. -->",
    "",
    llmsText,
    ""
  ];
  for (const link of links) {
    const { absolute } = resolveContainedPath(packageRoot, link, "llms.txt link");
    const body = fs.readFileSync(absolute, "utf8").trimEnd();
    sections.push("---", "", `# Source: ${link}`, "", body, "");
  }
  return `${sections.join("\n").trimEnd()}\n`;
}

function main() {
  fs.writeFileSync(outputPath, buildLlmsFullText(), "utf8");
  console.log(`Wrote ${path.relative(packageRoot, outputPath)}`);
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main();
}
